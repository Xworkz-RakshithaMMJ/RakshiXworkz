package com.xworkz.Vegetable;

public class Vegetable {
		private String name;
		private int price;
		private String typeOfVege;
		
		public String getName(){
			return name;
		}
		public void setName(String name){
			this.name = name;
		}
		public int getPrice(){
			return price;
		}
		public void setPrice(int price){
			this.price = price;
		}
		public String getTypeOfVege(){
			return typeOfVege;
		}
		public void setTypeOfVege(String typeOfVege){
			this.typeOfVege = typeOfVege;
		}
}
